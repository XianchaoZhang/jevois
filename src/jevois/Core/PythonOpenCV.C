  int failmsg(const char *fmt, ...) {
    char str[1000];
    va_list ap;
    va_start(ap, fmt);
    vsnprintf(str, sizeof(str), fmt, ap);
    va_end(ap);
    PyErr_SetString(PyExc_TypeError, str);
    return 0;
  }

